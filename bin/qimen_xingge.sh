set -euo pipefail

_resolve_link() {
  local f="$1"
  while [[ -L "$f" ]]; do
    local dir="$(cd "$(dirname "$f")" && pwd)"
    f="$(readlink "$f")"
    [[ "$f" != /* ]] && f="$dir/$f"
  done
  echo "$f"
}

SCRIPT_DIR="$(cd "$(dirname "$(_resolve_link "${BASH_SOURCE[0]}")")" && pwd)"
BASE_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

INPUT_PATH="./qmen_birth.json"
OUTPUT_PATH="./qmen_xingge.json"
_SHOW_WANWU=""

show_help() {
  cat <<'HELP'
Usage: qimen_xingge.sh [OPTIONS]

出生局性格分析 — 内在·外在性格取象

Options:
  --input=PATH            Input birth plate JSON (default: ./qmen_birth.json)
  --output=PATH           Output analysis JSON (default: ./qmen_xingge.json)
  --wanwu                 Show wanwu (万物类象) in text output
  -h, --help              Show this help

Requires: ./qmen_birth.json (generated by: qimen.sh --type=birth "YYYY-MM-DD HH:MM")
HELP
}

while (( $# > 0 )); do
  case "$1" in
    --input=*)            INPUT_PATH="${1#--input=}"; shift ;;
    --output=*)           OUTPUT_PATH="${1#--output=}"; shift ;;
    --wanwu)              _SHOW_WANWU="true"; shift ;;
    -h|--help)            show_help; exit 0 ;;
    *)                    echo "Unknown option: $1" >&2; exit 1 ;;
  esac
done

if [[ ! -f "$INPUT_PATH" ]]; then
  echo "Error: plate not found: $INPUT_PATH" >&2
  echo "Generate it first: qimen.sh --type=birth \"YYYY-MM-DD HH:MM\"" >&2
  exit 1
fi

source "$BASE_DIR/lib/data_loader.sh"

# 先加载输入盘面 JSON（供默认键读取），再加载性格类象数据文件。
dl_load_file "$INPUT_PATH"
dl_load_file "$BASE_DIR/data/wanwu_huaqizhen.dat"

source "$BASE_DIR/lib/qimen_xingge.sh"

xg_run_analysis "$INPUT_PATH" "$OUTPUT_PATH"
echo "Xingge analysis written to: $OUTPUT_PATH" >&2
